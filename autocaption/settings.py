# Database
# ---------------------------------------------------------------------------
# Defaults to SQLite for local dev. Set DATABASE_URL (or individual env vars)
# for PostgreSQL in production.
_db_url = os.environ.get("DATABASE_URL", "")
if _db_url:
    import re

    _m = re.match(
        r"postgres(?:ql)?://(?P<user>[^:]+):(?P<pass>[^@]+)@"
        r"(?P<host>[^:/]+)(?::(?P<port>\d+))?/(?P<name>.+)",
        _db_url,
    )
    if _m:
        DATABASES = {
            "default": {
                "ENGINE": "django.db.backends.postgresql",
                "NAME": _m.group("name"),
                "USER": _m.group("user"),
                "PASSWORD": _m.group("pass"),
                "HOST": _m.group("host"),
                "PORT": _m.group("port") or 5432,
            }
        }
    else:
        raise ValueError("Invalid DATABASE_URL format – expected PostgreSQL DSN.")
else:
    DATABASES = {
        "default": {
            "ENGINE": "django.db.backends.sqlite3",
            "NAME": BASE_DIR / "db.sqlite3",
        }
    }

